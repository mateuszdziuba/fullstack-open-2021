const typeDefs = gql`
  type Book {
    title: String!
    author: Author!
    published: Int!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    born: Int
    bookCount: Int!
  }

  type Query {
    bookCount(author: String): Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book!
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`

const resolvers = {
  Author: {
    bookCount: async (root, args) => {
      return Book.find({ author: root }).count()
    }
  },
  Query: {
    bookCount: async (root, args) => {
      if (!args.author) return Book.collection.countDocuments()
      // return books.reduce((acc, obj) => acc + (obj.author === args.author ? 1 : 0), 0)
    },
    authorCount: async () => Author.collection.countDocuments(),
    allBooks: async (root, args) => {
      if (!args.author && !args.genre) return Book.find({})
      if (!args.genre) {
        return Book.find({ author: args.author })
      }
      if (!args.author) {
        return Book.find({ genres: args.genre })
      }
      return Book.find({ author: args.author, genres: args.genre })
    },
    allAuthors: async () => { return Author.find({}) }
  },
  Mutation: {
    addBook: async (root, args) => {
      let author = await Author.exists( { name: args.author })
      if (!author) {
        author = new Author({ name: args.author, born: null })
        await author.save()
      } else {
        author = Author.findOne({ name: args.author })
      }
      let book = new Book({ ...args, author })
      await book.save()
      return book
    },
    editAuthor: async (root, args) => {
      const author = Author.findOne({ name: args.name })
      if (!author) {
        return null
      }

      author.born = args.setBornTo 
      await author.save()
      return author
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`)
})